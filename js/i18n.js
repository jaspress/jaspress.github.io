  function applyLang(lang) {
    var t = translations[lang] || translations.zh;
    document.querySelectorAll("[data-i18n]").forEach(function (el) {
      var key = el.getAttribute("data-i18n");
      if (t[key] !== undefined) el.textContent = t[key];
    });
    document.querySelectorAll("[data-i18n-html]").forEach(function (el) {
      var key = el.getAttribute("data-i18n-html");
      if (t[key] !== undefined) el.innerHTML = t[key];
    });
    document.querySelectorAll("[data-i18n-placeholder]").forEach(function (el) {
      var key = el.getAttribute("data-i18n-placeholder");
      if (t[key] !== undefined) el.setAttribute("placeholder", t[key]);
    });
    document.querySelectorAll("[data-i18n-aria]").forEach(function (el) {
      var key = el.getAttribute("data-i18n-aria");
      if (t[key] !== undefined) el.setAttribute("aria-label", t[key]);
    });
    /* Show/hide language-specific content blocks */
    document.querySelectorAll("[data-lang]").forEach(function (el) {
      el.style.display = (el.getAttribute("data-lang") === lang) ? "" : "none";
    });
    /* Set html lang attribute */
    if (lang === "en") {
      document.documentElement.lang = "en";
    } else if (lang === "zh-TW") {
      document.documentElement.lang = "zh-TW";
    } else {
      document.documentElement.lang = "zh-CN";
    }
    /* Update toggle button label: zh→EN, en→繁, zh-TW→简 */
    var btn = document.getElementById("lang-switch");
    if (btn) {
      if (lang === "zh") btn.textContent = "EN";
      else if (lang === "en") btn.textContent = "繁";
      else btn.textContent = "简";
    }
    localStorage.setItem(STORAGE_KEY, lang);
  }

  var savedLang = localStorage.getItem(STORAGE_KEY) || "zh";
  if (savedLang !== "zh") applyLang(savedLang);

  document.addEventListener("DOMContentLoaded", function () {
    var btn = document.getElementById("lang-switch");
    if (!btn) return;
    var currentLang = localStorage.getItem(STORAGE_KEY) || "zh";
    if (currentLang === "zh") btn.textContent = "EN";
    else if (currentLang === "en") btn.textContent = "繁";
    else btn.textContent = "简";
    btn.addEventListener("click", function () {
      var lang = localStorage.getItem(STORAGE_KEY) || "zh";
      var next = lang === "zh" ? "en" : (lang === "en" ? "zh-TW" : "zh");
      applyLang(next);
    });
  });
